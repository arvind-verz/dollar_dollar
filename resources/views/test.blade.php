                <div id="" class="ps-criteria-detail__content">
                    <h5><strong>Salary (Mandatory)</strong></h5>
                    <ul class="ps-list--arrow-circle">
                        <li><span style="text-decoration: underline;">Salary is mandatory criteria</span> for Bonus Rate in&nbsp; DBS Multiplier Account</li>
                        <li>Only salary credit transaction made via GIRO reflected as 'GIRO - SALARY' or other transaction code determine by DBS will be considered</li>
                        <li>Total amount of eligible salary credit will be considered if there is two or more salary crediting (E.g Total $8K = $5k from A + $2K from B)</li>
                        <li>Salary can credit to any personal or joint DBS/POSB deposit account and do not have to be Multiplier Account</li>
                    </ul>
                </div>
                <div id="" class="ps-criteria-detail__content">
                    <h5><strong>Card Spent</strong></h5>
                    <ul class="ps-list--arrow-circle">
                        <li>Spend on selected DBS/POSB Credit Card (E.g POSB Everyday, DBS Live Fresh, etc..) will qualify for card spend</li>
                        <li>Only retail transactions and cash advance transactions will contribute to card spent, refer to DBS for exclusion of transactions</li>
                        <li>Eligible transaction made on supplementary credit card are accorded to principal credit card holder</li>
                        <li>Eligible transaction is recognized by transaction posting date to credit card account. (E.g Spend on 30 Aug, Posted on 1 Sep. Transaction recognized under Sep)</li>
                        <li>Reversal OR Refund will offset eligible card spend. (E.g Refund from retail shop or Reversal of unsuccessful transaction)</li>
                    </ul>
                </div>
                <div id="" class="ps-criteria-detail__content">
                    <h5><strong>Home Loan</strong></h5>
                    <ul class="ps-list--arrow-circle">
                        <li>Monthly Instalment due on home loan with DBS/POSB will be recognized &nbsp;</li>
                        <li>First 3 joint borrowers of the home loan will each be accorded the full amount of monthly installment due.This amount is not divided among borrowers</li>
                        <li>Both CPF and Cash contributions for the home loan will be included in eligible transaction amount</li>
                    </ul>
                </div>
                <div id="" class="ps-criteria-detail__content">
                    <h5><strong>Insurance</strong></h5>
                    <ul class="ps-list--arrow-circle">
                        <li>Life Insurance Regular Premium policies purchase through DBS/POSB are eligible &nbsp;</li>
                        <li>Purchase made after Multiplier Account is successfully opened will be recognised as eligible transaction <strong>AND</strong>&nbsp;Recognition will start 1 month after policy inception date for 12 consecutive months PER Insurance</li>
                        <li>Annual Premium are divided by 12 to determine monthly eligible transaction amount under Insurance criteria</li>
                        <li>Policy will still be recognised as eligible transaction as long as policy is still in force</li>
                        <li>Please refer to <a href="https://www.dbs.com.sg/iwov-resources/pdf/deposits/multiplier-programme/DBS%20Multiplier%20Programme_FAQ.pdf" target="_blank" rel="noopener"><span style="text-decoration: underline;">DBS</span></a> for full details on this criteria</li>
                    </ul>
                </div>
                <div id="" class="ps-criteria-detail__content">
                    <h5><strong>Investment</strong></h5>
                    <ul class="ps-list--arrow-circle">
                        <li>Invest in the following eligible investment: Dividend Credit, Unit Trust (UT) and Online Trade Equities (OTE)</li>
                        <li>Dividend Credit: Combined total dividend credits in the joint account will be recognized</li>
                        <li>UT: CPF and SRS Funds to purchase are not eligible, only cash is eligible <strong>AND</strong> Only UT purchased after successfully open Multiplier account is recognized as eligible transaction</li>
                        <li>UT:&nbsp; Lapsed in monthly contribution&nbsp; <strong>AND</strong> Repurchase within 6 months from date of termination&nbsp;will no longer recognised as eligible transactions</li>
                        <li>Please refer to <a href="https://www.dbs.com.sg/iwov-resources/pdf/deposits/multiplier-programme/DBS%20Multiplier%20Programme_FAQ.pdf" target="_blank" rel="noopener"><span style="text-decoration: underline;">DBS</span></a> for full details on this criteria</li>
                    </ul>
